				$.ajax({
					url: baseDir+"Carrito/eliminar",
					type: 'GET',
					data: {
						id: idCarrito
					},
					success: function(){
						$('#articulo'+idCarrito).fadeOut('slow', function() {
									$.scrollTo(this, 2000);
